import Foundation

enum Temperature : String, CaseIterable {
    case 多冰 = "多冰 More Ice"
    case 正常冰 = "正常冰 Regular Ice"
    case 少冰 = "少冰 Less Ice"
    case 微冰 = "微冰 Half Ice"
    case 去冰 = "去冰 Ice-Free"
    case 完全去冰 = "完全去冰 Without Ice"
    case 常溫 = "常溫 Room Temperature"
    case 溫 = "溫 Warm"
    case 熱 = "熱 Hot"
}
